#Ввести число, вивести його розряди та їх множники
print("Введіть число")
y = int(input())

if y < 10:
    print(y,'*10^0')
elif 10 < y < 100:
    print(y//10,'*10^1','+', y-y//10*10, '*10^0')
if 100 < y < 1000:
    print(y//100,'*10^2','+',y//10,'*10^1','+', y-y//10*10, '*10^0')
else:
    print("very very big number")
